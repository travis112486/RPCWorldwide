// Short labels for mobile bottom bar
const mobileLabels: Record<string, string> = {
  'My Profile': 'Profile',
  'Casting Calls': 'Castings',
  'My Applications': 'Applications',
};

export function Sidebar({ role = 'talent' }: { role?: 'talent' | 'admin' }) {
  const pathname = usePathname();
  const [collapsed, setCollapsed] = useState(false);
  const links = role === 'admin' ? adminLinks : talentLinks;

  return (
    <>
      {/* Desktop sidebar */}
      <aside
        className={cn(
          'hidden h-[calc(100vh-4rem)] border-r border-border bg-muted/30 transition-all duration-200 lg:block',
          collapsed ? 'w-16' : 'w-56',
        )}
      >
        <div className="flex h-full flex-col">
          <div className="flex items-center justify-end p-2">
            <button
              onClick={() => setCollapsed(!collapsed)}
              className="rounded-md p-1.5 text-muted-foreground hover:bg-secondary hover:text-foreground"
              aria-label={collapsed ? 'Expand sidebar' : 'Collapse sidebar'}
            >
              <svg className={cn('h-4 w-4 transition-transform', collapsed && 'rotate-180')} fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={2}>
                <path strokeLinecap="round" strokeLinejoin="round" d="M15.75 19.5L8.25 12l7.5-7.5" />
              </svg>
            </button>
          </div>

          <nav className="flex-1 space-y-1 px-2">
            {links.map((link) => {
              const active = pathname === link.href || pathname.startsWith(link.href + '/');
              return (
                <Link
                  key={link.href}
                  href={link.href}
                  className={cn(
                    'flex items-center gap-3 rounded-lg px-3 py-2 text-sm font-medium transition-colors',
                    active
                      ? 'bg-primary/10 text-primary'
                      : 'text-muted-foreground hover:bg-secondary hover:text-foreground',
                    collapsed && 'justify-center px-2',
                  )}
                  title={collapsed ? link.label : undefined}
                >
                  {link.icon}
                  {!collapsed && <span>{link.label}</span>}
                </Link>
              );
            })}
          </nav>
        </div>
      </aside>

      {/* Mobile bottom tab bar */}
      <nav className="fixed inset-x-0 bottom-0 z-40 border-t border-border bg-background/95 backdrop-blur supports-[backdrop-filter]:bg-background/80 lg:hidden">
        <div className="mx-auto flex h-14 max-w-lg items-center justify-around px-2">
          {links.map((link) => {
            const active = pathname === link.href || pathname.startsWith(link.href + '/');
            const shortLabel = mobileLabels[link.label] ?? link.label;
            return (
              <Link
                key={link.href}
                href={link.href}
                className={cn(
                  'flex flex-1 flex-col items-center gap-0.5 rounded-md py-1 text-[10px] font-medium transition-colors',
                  active
                    ? 'text-primary'
                    : 'text-muted-foreground hover:text-foreground',
                )}
              >
                <span className="[&>svg]:h-5 [&>svg]:w-5">{link.icon}</span>
                {shortLabel}
              </Link>
            );
          })}
        </div>
      </nav>
    </>
  );
}
